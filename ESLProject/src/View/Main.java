    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        String type= typeComboBox.getSelectedItem().toString();
        String bangla= banglaText.getText();
        String english= engText.getText();
        
        AddIntoDB addDB=new AddIntoDB();
        
        if(!bangla.equals("") && !english.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Information Successfully Added..");
            addDB.InsertToDB(type, bangla, english);
            banglaText.setText("");
            engText.setText("");
            
            SetBanglaList();
            
          
        }
        else if(bangla.equals("") && english.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Blank is not acceptable !");
        }
        else if(bangla.equals("") || english.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Blank is not acceptable !!");
        }
    }//GEN-LAST:event_jButton9ActionPerformed

