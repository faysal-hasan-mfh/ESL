    private void TotalListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_TotalListValueChanged
        String tmp= (String) TotalList.getSelectedValue();
      if(banglaButt.isSelected())
       {
        String sql= "select * from ESL where bangla=?";

        try
        {
            pst=conn.prepareStatement(sql);
            pst.setString(1, tmp);
            rs=pst.executeQuery();
            if(rs.next())
            {
                String bangla=rs.getString("bangla");
                String english=rs.getString("english");

                banglaTextArea.setText(bangla);
                englishTextArea.setText(english);
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
       }
        else
        {
        String sql= "select * from ESL where english=?";

        try
        {
            pst=conn.prepareStatement(sql);
            pst.setString(1, tmp);
            rs=pst.executeQuery();
            if(rs.next())
            {
                String bangla=rs.getString("bangla");
                String english=rs.getString("english");

                banglaTextArea.setText(bangla);
                englishTextArea.setText(english);
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
        }
    }//GEN-LAST:event_TotalListValueChanged

